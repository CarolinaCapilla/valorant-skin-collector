<template>
	<UAuthForm
		:fields="fields"
		:schema="schema"
		:providers="providers"
		title="Create an account"
		:submit="{ label: 'Create account' }"
		@submit="onSubmit"
	>
		<template #description>
			Already have an account? <ULink to="/auth/login" class="text-primary font-medium">Login</ULink>.
		</template>

		<template #footer>
			By signing up, you agree to our
			<ULink to="/" class="text-primary font-medium">Terms of Service</ULink>.
		</template>
	</UAuthForm>
</template>

<script setup lang="ts">
import * as z from 'zod'
import type { FormSubmitEvent } from '@nuxt/ui'
import { useAuthStore } from '~/stores/auth'

definePageMeta({
	layout: 'auth'
})

useSeoMeta({
	title: 'Sign up',
	description: 'Create an account to get started'
})

const notify = useNotification()
const router = useRouter()
const authStore = useAuthStore()

const fields = [
	{
		name: 'name',
		type: 'text' as const,
		label: 'Name',
		placeholder: 'Enter your name'
	},
	{
		name: 'email',
		type: 'text' as const,
		label: 'Email',
		placeholder: 'Enter your email'
	},
	{
		name: 'password',
		label: 'Password',
		type: 'password' as const,
		placeholder: 'Enter your password'
	},
	{
		name: 'password_confirmation',
		label: 'Confirm Password',
		type: 'password' as const,
		placeholder: 'Confirm your password'
	}
]

const providers = [
	{
		label: 'Google',
		icon: 'i-simple-icons-google',
		onClick: () => {
			authStore.redirectToOAuthProvider('google')
		}
	},
	{
		label: 'GitHub',
		icon: 'i-simple-icons-github',
		onClick: () => {
			authStore.redirectToOAuthProvider('github')
		}
	}
]

const schema = z.object({
	name: z.string().min(1, 'Name is required'),
	email: z.string().email('Invalid email'),
	password: z.string().min(8, 'Must be at least 8 characters'),
	password_confirmation: z.string().min(8, 'Must be at least 8 characters')
})

type Schema = z.output<typeof schema>

async function onSubmit(payload: FormSubmitEvent<Schema>) {
	try {
		await authStore.register({
			name: payload.data.name,
			email: payload.data.email,
			password: payload.data.password,
			password_confirmation: payload.data.password_confirmation
		})

		notify.success('Success', 'Registered successfully')

		// Redirect to home or dashboard
		router.push('/')
	} catch (error) {
		const errorMessage =
			error && typeof error === 'object' && 'data' in error
				? (error.data as { message?: string })?.message
				: 'Login failed. Please check your credentials.'

		notify.error('Error', errorMessage)
	}
}
</script>
